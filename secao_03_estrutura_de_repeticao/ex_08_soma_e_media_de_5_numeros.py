"""
Exercício 08 da seção de estrutura sequencial da Python Brasil:
https://wiki.python.org.br/EstruturaDeRepeticao

Faça um programa que leia 5 números e informe a soma e a média dos números.

Mostre a média com duas casas decimais.

    >>> calcular_soma_e_media(1, 2, 3, 4, 5)
    'Soma: 15, Média: 3.00'
    >>> calcular_soma_e_media(-1, -30, -20, -15, -2)
    'Soma: -68, Média: -13.60'

"""


def calcular_soma_e_media(n1: int, n2: int, n3: int, n4: int, n5: int) -> str:
    """Escreva aqui em baixo a sua solução"""
    soma = n1 + n2 + n3 + n4 + n5
    media = soma / 5
    print(f"'Soma: {soma:.0f}, Média: {media:.2f}'")
        